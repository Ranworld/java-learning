package day5;

public class OperatorsDemo5 {

	public static void main(String[] args) {
		// Relational Operator == equal != not equal > < >= greater than equal to <= less than equal to
		
		int a = 4;
		int b = 5;
		
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println(a>b);
		System.out.println(a<b);
		

	}

}
