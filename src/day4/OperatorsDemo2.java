package day4;

public class OperatorsDemo2 {

	public static void main(String[] args) {
		
	
		// Assignment Operators: += -= /= *= % = 	
		// associativity : Right to Left
		
		int a = 4 + 5 - 6 / 2 - 3 * 2 + 7 % 3 ;
		System.out.println(a);
		
		int i = 11;
		System.out.println(i);
		i = i + 4;
		System.out.println(i);
		
		// for shortcut method
		i += 4;
		System.out.println(i);
		
		

	}

}
